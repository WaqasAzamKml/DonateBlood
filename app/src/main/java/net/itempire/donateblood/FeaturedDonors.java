public class FeaturedDonors {
    private String donorName, donoroThanks, donorCity;
    public FeaturedDonors(){}

    public String getDonorName() {
        return donorName;
    }

    public void setDonorName(String donorName) {
        this.donorName = donorName;
    }

    public String getDonoroThanks() {
        return donoroThanks;
    }

    public void setDonoroThanks(String donoroThanks) {
        this.donoroThanks = donoroThanks;
    }

    public String getDonorCity() {
        return donorCity;
    }

    public void setDonorCity(String donorCity) {
        this.donorCity = donorCity;
    }
}
